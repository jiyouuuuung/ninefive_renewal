					<p class="desc">휴대폰과 태블릿을 가지고 있는 워킹맘을 대상으로 시작된 프로젝트로, 워킹맘의 육아 문제에 도움이 되고자 개발되었습니다. 태블릿과 모바일폰에 어플리케이션을 설치하고, 태블릿은 보모(Baby Sitter)가, 모바일은 워킹맘이 사용하는 시스템입니다. 태블릿 어플리케이션의 경우 보모가 사용하기 때문에, 최대한 단순하고 직관적으로 UI를 구성하였습니다. 육아 일지를 기록하기 위해 보모가 해야 할 거의 유일한 인터랙션은 ‘Drag&Drop’으로, 메인 화면의 정중앙으로 기록할 항목의 아이콘을 드래그하면, 아이템의 종류와 시간이 자동으로 기록됩니다. 또한 원한다면 수동으로 수정할 수도 있습니다. 기록된 데이터는 원형 GUI 에 기록된 시간/아이템의 유형이 표시됩니다. 모든 데이터는 실시간 알림으로 엄마에게 전송되며, 엄마의 모바일 어플리케이션은 Setting에 대한 온전한 권한을 갖는 대신에, 육아 일지 기록 관련해서는 Viewing만 가능한 시스템입니다.<br /><br /> *독특한 UI와 컨셉으로 2015 iF Design Award 수상</p>
